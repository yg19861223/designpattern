package main.com.fanxing;

public class Dog {
    private int head;
    
    private int foot;

    public Dog(int head,int foot){
		this.head=head;
		this.foot=foot;
	}
	public int getHead() {
		return head;
	}

	public void setHead(int head) {
		this.head = head;
	}

	public int getFoot() {
		return foot;
	}

	public void setFoot(int foot) {
		this.foot = foot;
	}
    
}
