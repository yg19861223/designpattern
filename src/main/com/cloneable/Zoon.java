package main.com.cloneable;

public class Zoon {
   private String eys;
   private String mouth;
   public Zoon(String eys,String mouth){
	   this.eys=eys;
	   this.mouth=mouth;
   }
   
public String getEys() {
	return eys;
}
public void setEys(String eys) {
	this.eys = eys;
}
public String getMouth() {
	return mouth;
}
public void setMouth(String mouth) {
	this.mouth = mouth;
}
   
}
